import math

pi = 3.14
x = 1
y = 2
z = 3


print(round(pi)) #Arredonda para o proximo
print(math.ceil(pi)) #Aproxima para o proximo numero mais alto
print(math.floor(pi)) #Abaixa para o numero mais baixo
print(abs(pi)) #positiva o numero, tornando ele positivo
print(pow(pi,2)) #eleva ele a algum numero
print(math.sqrt(pi)) #aplica a raiz quadrada a esse numero
print(max(x,y,z,pi)) #localiza o maior numero
print(min(x,y,z,pi)) #localiz o menor numero

